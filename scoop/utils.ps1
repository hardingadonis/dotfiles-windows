function Install-Package {
	param (
		[string]$packageName
	)

	if (-not (scoop list | Select-String -Pattern $packageName)) {
		Write-Output "$packageName is not installed. Installing..."
		scoop install $packageName
	} else {
		Write-Output "$packageName is already installed."
	}
}
